fn hex_to_field(hex_str: &str) -> F<G1> {
  // Remove "0x" prefix if present
  let hex_str = hex_str.trim_start_matches("0x");

  // Pad with leading zero if needed to ensure even length
  let hex_str = if hex_str.len() % 2 == 1 { format!("0{}", hex_str) } else { hex_str.to_string() };

  // Convert to bytes
  let bytes = hex::decode(hex_str).unwrap();

  // Ensure we have 32 bytes by padding with zeros if needed
  let mut padded_bytes = vec![0u8; 32];
  let start = padded_bytes.len() - bytes.len();
  padded_bytes[start..].copy_from_slice(&bytes);

  // Convert to u64 array
  let mut u64s = [0u64; 4];
  for i in 0..4 {
    let mut chunk = [0u8; 8];
    chunk.copy_from_slice(&padded_bytes[i * 8..(i + 1) * 8]);
    u64s[3 - i] = u64::from_be_bytes(chunk);
  }

  F::<G1>::from_raw(u64s)
}

